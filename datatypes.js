// Different types of datatypes:

// 1. Number - If you want to perform any type of math.
let x = 7;
console.log(typeof x); // this prints to console the word 'number'. 

// 2. Boolean - If you want to do yes/no or true/false evaluation.
let y = true; // true or false
console.log(typeof y); // this prints to console the word 'boolean'.

// 3. String - If you want to display something on screen (string is shorthand for 'string of characters')
let z = 'hello world'; // single quotes
console.log(typeof z); // this prints to console the word 'string'.

// 4. undefined 
let a; 
console.log(a); // prints to console the word 'undefined'.
console.log(typeof a); // prints to console the word 'undefined'.